export function listMockAuditByMatter(matterId: string): PaginatedAuditResult {
  const items = mockAuditEvents
    .filter((event) => event.matterId === matterId)
    .sort((left, right) => Date.parse(right.createdAt) - Date.parse(left.createdAt))
    .map(cloneAuditEvent);

  return {
    items,
    page: 1,
    perPage: items.length || 25,
    total: items.length,
  };
}

function cloneAuditEvent(event: AuditEvent): AuditEvent {
  return {
    ...event,
    diff: event.diff ? { ...event.diff } : null,
    diffEntries: event.diffEntries.map((entry) => ({ ...entry })),
    relatedItems: event.relatedItems.map((item) => ({ ...item })),
  };
}
